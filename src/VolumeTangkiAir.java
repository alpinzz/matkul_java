public class VolumeTangkiAir {
    public static void main(String[] args) {
        double panjang, lebar, tinggi, volume;
        panjang = 3; // meter
        lebar = 0.8; // m
        tinggi = 0.6; // m

        // Volume = panjang * lebar * tinggi

        volume = panjang * lebar * tinggi; // m

        System.out.println(volume);

    }
}
